public class NoTry {
	//Chapter2 Java Language - 2.6 Lab2-6-1 NoTry
	public static void main(String[] args) {
		String msg[] = {"Kim", "Bo", "Ri"};
		int n = msg.length;
		
		for (int i = 0; i <= n; i++) {
			System.out.println(msg[i]);
		}
		System.out.println("Everything is done.");
		
	}

}
